    <h1 class="levHeader">Leverings- en Retourbeleid:</h1>
    <p class="levText">Levering:
        Bij BEFS streven we ernaar om jouw bestelling zo snel en efficiënt mogelijk te leveren, terwijl we tegelijkertijd rekening houden met het milieu. We maken gebruik van duurzame verzendmethoden en werken samen met betrouwbare bezorgdiensten die zich ook inzetten voor milieuvriendelijkheid. Zodra je bestelling is geplaatst, doen we ons best om deze binnen de aangegeven tijd bij jou thuis te bezorgen. Houd er rekening mee dat de levertijd kan variëren afhankelijk van je locatie en eventuele externe omstandigheden.

        Retourneren:
        We hopen dat je tevreden bent met je aankoop bij BEFS. Mocht je echter niet helemaal tevreden zijn of een probleem hebben met het ontvangen product, neem dan zo snel mogelijk contact met ons op. We streven ernaar om eventuele problemen naar tevredenheid op te lossen.

        Hier zijn enkele belangrijke punten met betrekking tot het retourneren:

        Retouraanvraag: Stuur ons binnen 14 dagen na ontvangst van je bestelling een e-mail met je bestelgegevens en de reden voor de retournering. We zullen je dan voorzien van verdere instructies.

        Ongeopende en onbeschadigde producten: Om in aanmerking te komen voor retournering, moeten de producten in hun originele staat zijn, ongeopend en onbeschadigd. Zorg ervoor dat alle verpakkingen en labels intact zijn.

        Verzendkosten: Houd er rekening mee dat de kosten voor het retourneren van producten voor jouw rekening zijn, tenzij het een fout van ons betreft of het product beschadigd of defect is bij aankomst.

        Terugbetaling: Zodra we het geretourneerde product hebben ontvangen en gecontroleerd, zullen we overgaan tot terugbetaling van het aankoopbedrag. Dit gebeurt via de oorspronkelijke betaalmethode die je hebt gebruikt bij het plaatsen van je bestelling.

        Belangrijk:

        Let op eventuele specifieke retourinstructies die we je kunnen verstrekken, afhankelijk van het type product dat je hebt gekocht.
        We kunnen geen retourzendingen accepteren voor bederfelijke producten vanwege hygiënische redenen.
        Neem bij vragen of opmerkingen over ons leverings- en retourbeleid gerust contact met ons op. We staan klaar om je te helpen en ervoor te zorgen dat je een positieve ervaring hebt met BEFS.</p>
